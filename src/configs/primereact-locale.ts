/**
 * 获取当前语言的 PrimeReact 配置
 * @param language - i18next 语言代码
 * @returns PrimeReact 语言配置对象
 */
export function getPrimeReactLocale(language: string) {
  return primeReactLocales[language] || enLocale
}
